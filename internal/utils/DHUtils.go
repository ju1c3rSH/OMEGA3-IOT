// DHService 提供 DH 模幂运算服务
type DHService struct {
	params *DHParams
}

// NewDHService 创建 DH 服务实例
func NewDHService(params *DHParams) *DHService {
	return &DHService{params: params}
}

// Params 返回 DH 参数
func (s *DHService) Params() *DHParams {
	return s.params
}

// passwordToExponent 将密码转换为指数（SHA256 哈希 → 大整数）
func (s *DHService) passwordToExponent(password string) *big.Int {
	hash := sha256.Sum256([]byte(password))
	return new(big.Int).SetBytes(hash[:])
}

// ComputeCommitment 计算承诺值 A = g^SHA256(password) mod p
// 注册时调用，结果存入数据库
func (s *DHService) ComputeCommitment(password string) *big.Int {
	exponent := s.passwordToExponent(password)
	return new(big.Int).Exp(s.params.G, exponent, s.params.P)
}

// ComputeProof 计算证明值 B = commitment^nonce mod p
// 登录时客户端调用
func (s *DHService) ComputeProof(commitment, nonce *big.Int) *big.Int {
	return new(big.Int).Exp(commitment, nonce, s.params.P)
}

// VerifyProof 验证 proof == commitment^nonce mod p
// 登录时服务端调用
func (s *DHService) VerifyProof(proof, commitment, nonce *big.Int) bool {
	expected := s.ComputeProof(commitment, nonce)
	return expected.Cmp(proof) == 0
}

// GenerateNonce 生成随机 Nonce（256-bit）
func GenerateNonce() (*big.Int, error) {
	nonce, err := rand.Int(rand.Reader, new(big.Int).Lsh(big.NewInt(1), 256))
	if err != nil {
		return nil, fmt.Errorf("failed to generate nonce: %w", err)
	}
	return nonce, nil
}

// BigIntToHex 将 big.Int 转换为十六进制字符串
func BigIntToHex(n *big.Int) string {
	return fmt.Sprintf("%x", n.Bytes())
}

// HexToBigInt 将十六进制字符串转换为 big.Int
func HexToBigInt(hexStr string) (*big.Int, error) {
	n, ok := new(big.Int).SetString(hexStr, 16)
	if !ok {
		return nil, fmt.Errorf("invalid hex string: %s", hexStr)
	}
	return n, nil
}
